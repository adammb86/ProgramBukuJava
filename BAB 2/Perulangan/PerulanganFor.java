public class PerulanganFor {
    public static void main(String args[]) {
        // For ascending
        System.out.println("For Ascending");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        // For descending
        System.out.println("For Descending");
        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
